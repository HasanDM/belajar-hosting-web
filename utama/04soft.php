			<br>
			&emsp;&emsp;&emsp;Beberapa waktu lalu, Microsoft meluncurkan Surface Laptop sekaligus OS terbaru mereka, Windows 10 S. Bila sahabat kurang mengikuti perkembangan Windows 10 Cloud, maka akan banyak pertanyaan tentang perbedaan dari OS terbaru Microsoft ini. Sebab itulah, kali ini beritateknologi ingin menyampaikan lagi kelebihan dan kekurangan Windows 10 S berbasis Cloud-computing yang diklaim bisa menyaingi perangkat Chromebook ini. 
			<h2>Kelebihan Windows 10 S</h2>
			
			<h5>Perangkat Lebih Murah</h5>
			&emsp;&emsp;Ya, semua perangkat Windows 10 S akan dijual lebih murah untuk menyaingi notebook dan laptop Chromebook. Bahkan setelah Microsoft Surface Laptop dirilis dan Windows 10 S resmi diluncurkan untuk para vendor, harga jual yang memungkinkan dipatok mulai $189 atau hanya sekitar 2,5 juta rupiah!

			<h5>Performa Cepat</h5>
			<center><img src="images/Microsoft-Surface-Laptop.jpg"></center>
			<br>
			&emsp;&emsp;Mengutamakan pasar pendidikan, maka kecepatan menjadi prioritas. OS ini berjalan ringan, hingga memungkinkan Windows 10 S untuk siap digunakan dalam waktu 15 detik saja. Bahkan, proses penginstallan pun sangat mudah dan ringan, bisa hanya bermodal stik USB saja.

			<h5>Baterai Lebih Awet</h5>

			&emsp;&emsp;Memiliki daya hidup yang lebih hemat dari Windows 10, maka baterai perangkat pun menjadi lebih awet. Apalagi, alternatif dari Chromebook ini hanya akan diusung oleh perangkat portabel yang memiliki layar kecil, dan tidak memiliki kegunaan prioritas untuk bermain game, ataupun menjalankan aplikasi berat.

			<h5>Punya Segala Fitur Windows 10 Pro</h5>

			&emsp;&emsp;Beruntung, Windows 10 S dibangun berdasarkan platform Windows 10 Pro, bukan dari Windows 10 Home. Macam-macam fitur seperti Azure Active Directory (AD), Mobile Device Management (MDM), dan lain-lain masih bisa didapatkan di Windows 10 S ini. 

			<h5>Bisa Diinstall di Segala Perangkat Laptop dan PC</h5>
			<br>
			<center><img src="images/hp-windows-10-s.jpg"></center>
			<br>
			&emsp;&emsp;Ya, inilah kelebihannya dari Chromebook yang memakai mesin khusus, Windows 10 S bisa diinstall di segala perangkat laptop ataupun PC. Bahkan bila sahabat mempunyai laptop lawas yang sudah tak mumpuni dipasangi Windows 10 Pro, pasang saja Windows 10 S. Dijamin menjadi cepat dan ringan! Perangkat Windows 10 S dari HP Segera Hadir 
			<h2>Kekurangan Windows 10 S</h2>

			<h5>Aplikasi EXE tidak diperbolehkan</h5>

			&emsp;&emsp;Seperti Chromebook yang mengharuskan segala aplikasi bersumber dari Play Store, begitupula dengan Windows 10 S. Kita tidak bisa menginstall program-program di luar Microsoft Store. Artinya, fleksibilitas OS Windows tidak bisa kita andalkan di OS ini. Walaupun untuk pendidikan, kita tak bisa lagi menginstall aplikasi pendidikan berat seperti MatLab, atau aplikasi pihak ketiga, misalnya dari developer lokal yang belum masuk toko Microsoft. 

			<h5>Selamat Tinggal, Chrome dan Firefox </h5>

			&emsp;&emsp;Monopoli terhadap web browser tidak dapat dipungkiri lagi oleh Microsoft. Dalam Windows 10 S ini, mereka menyebutkan tidak akan memperbolehkan web browser selain Microsoft Edge menjadi default browser. Dengan adanya ancaman ini, bahkan belum diketahui apakah Chrome dan Firefox akan tersedia di Microsoft Store atau tidak. Wah!
